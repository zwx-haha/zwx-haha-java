/**
 * <p></p>
 *
 * @author: haha
 * @date : 2020-09-02 17:14
 */
@EnableEurekaClient
@SpringBootApplication
public class Application {
    public static void main(String[] args) {
        SpringApplication application = new SpringApplication(Application.class);
        application.run(args);
    }
}
